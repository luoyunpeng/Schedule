package org.schedule.entity;

public class Level {
    private int level;
    private String leaderName;
    
    public int getLevel() {
        return level;
    }
    public String getLeaderName() {
        return leaderName;
    }
    public void setLeaderName(String leaderName) {
        this.leaderName = leaderName;
    }
    public void setLevel(int level) {
        this.level = level;
    }


}
